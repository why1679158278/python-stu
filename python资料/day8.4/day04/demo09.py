"""
    字符串格式化
        定义:
            生成一定格式的字符串
        基础语法:
            "固定格式"%(变量1,变量2...)

        占位符(类型码):%d      %s     %f
                     整数    字符串   小数

        辅助格式
            %.2d 整数使用2位,不足用0填充
            %.2f  小数使用2位,不足用0填充
"""
jin = 5
liang = 8
# 字符串拼接
print(str(jin) + "斤零" + str(liang) + "两")
# 字符串格式化
print("%d斤零%d两" % (jin, liang))







