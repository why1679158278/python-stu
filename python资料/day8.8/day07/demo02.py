"""
    集合set
        去重复
"""
# 创建
# -- 集合名 = {元素1,元素2,元素3}
set01 = {"悟空", "唐僧", "八戒"}

list01 = ["唐僧", "悟空", "唐僧", "八戒", "唐僧"]
# -- 集合名 = set(可迭代对象)
set02 = set(list01)
print(set02)

# 添加:集合名.add(元素)
set02.add("小白龙")

# 定位
#(因为无序不能使用索引切片)
#(因为不是键值对不能使用键查找键)

# 遍历
for item in set01:
    print(item)

# 删除
if "悟空1" in set01:
    set01.remove("悟空1")